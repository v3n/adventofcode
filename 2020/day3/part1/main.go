func main() {
	fragments := strings.Split(raw, "\n")
	height := len(fragments)
	width := len(fragments[0])

	grid := strings.Join(fragments, "")

	var (
		down = 1
		right = 3
	)

	trees := 0

	x, y := 0, 0

	coords := func(x, y int) int {
		return (y * width) + x
	}

	for {
		if y == height && x == width {
			break
		}

		if grid[coords(x, y)] == '#' {
			trees++
		}

		y += down
		x += right

		if y >= height {
			break
		}

		if x >= width {
			x = x - width
		}
	}

	fmt.Printf("Trees: %d\n", trees)
}
